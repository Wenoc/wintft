  <h1>Kai'Sa Reroll Guide</h1>
  <div class="blogBody">
    <p><strong>Note:</strong> This guide was written in patch 12.20</p>
    <p>
      If you want to play the same comp every game to gain LP or just to learn
      how to play Kai'Sa reroll, you're at the right place. Kai'Sa reroll is
      considered the best comp on the Chinese servers.
    </p>
    <p>
      This comp's strength is that it can be played every game and consistently
      be in top 4. Its weakness is that it relies on 3* units. (Kai'Sa, Zac)
    </p>
    <p>Kai'Sa is the primary carry, and Zac is the primary tank.</p>

    <h2>Items:</h2>
    <p>Carousel priority: tear > rod > glove > sword</p>
    <p>
      Kai'Sa: 1 mana regeneration item (Blue buff > Spear of Shojin), 2 dmg
      items (IE, JG > GS, Archangel's staff) Zac: Any tank items (Warmog's
      Armor, Ionic Spark, Bramble Vest)
    </p>
    <p>Put the rest of the items on Jax and Volibear.</p>
    <h2>Early game:</h2>
    <p>
      Because you play Lagoon the entire game, you should activate it as soon as
      possible.
    </p>
    <p>
      Stage 2 board example: Malphite, Taliyah, Kai'Sa, Nasus/Karma If you can't
      find Kai'Sa immediately, put your items on Taliyah.
    </p>
    <p>
      Win streak or lose streak through the stage, and try to make blue buff as
      early as possible.
    </p>
    <h2>Mid game:</h2>
    <p>
      In mid-game, your board will look something like this: <br /> Karma, Malphite,
      Nasus, Kai'Sa, Zac, Jax
    </p>
    <p>
      If you find Volibear, temporarily put it in instead of Karma. At level 6,
      you should start slow rolling to hit 3* Kai'sa and Zac.
    </p>
    <p>Around 4-5, if you found Zac / Kai'Sa 3*, level up to 7.</p>
    <h2>Late game:</h2>
    <p>
      As soon as you can go level 8, do it and activate the 4 Dragonmancers with
      Lee Sin.
    </p>
    <p>If you find Yasuo, replace Karma with it.</p>
    <p>
      If you manage to go to level 9, Bard is an excellent addition to the team.
    </p>
    <p>Positioning:</p>
    <p>
      Nasus and Karma are on the backline to protect Kai'sa from assassins. Jax
      in front of Kai'Sa.
    </p>
    <p>Zac is in the middle of the frontline.</p>

    <h2>Best Augments:</h2>
    <ul>
      <li>Featherweights I, II, III</li>
      <li>Preparation I, II, III</li>
      <li>Trade Sector</li>
      <li>Golden Ticket</li>
    </ul>
    <p>
      If you shared this guide with others, that would help very much, and I
      would greatly appreciate it.
    </p>
