<div style="" class="wrapAll">
  <h1>Leveling and Economy Guide</h1>
  <div class="blogBody">
    <p>
      Hey, If you have trouble deciding when to level up or roll and you never
      have enough gold, you are at the right place. Here you can learn how to
      properly manage your economy. I use a standard comp as an example, but you
      can find specific strategies for slow roll comps in the blog's final
      section.
    </p>
    <p>
      <strong>Note: </strong> This article is recommended for players below diamond.
      You probably already know and apply these tips in your games if you are diamond
      or above.
    </p>
    <h2>Stage 1</h2>
    <p>
      Sometimes you can get 10 gold if you're lucky, and you can get ahead of
      everyone else in gold interest and hit more powerful units earlier.
    </p>
    <p>
      A trick you can do at 1-3 is pre-level and get a level 4 shop in the next
      round. This allows you to see 3-cost units in the shop, but be careful
      because it lowers your chances of getting 1 and 2-cost units in the shop.
      Only do this if you don't need any more 1-cost units or are specifically
      looking for a 3-star character.
    </p>
    <h2>Stage 2</h2>
    <p>
      <strong>Note:</strong> If you choose an economy augment, your strategy will
      differ.
    </p>
    <p>
      The goal in the second stage is to build up as large of an economy as
      possible. <br /> You can gain gold in 4 different ways:
    </p>
    <ul>
      <li><p>Base income (5 gold)</p></li>
      <li><p>Streak Bonus (1-3 gold)</p></li>
      <li><p>PvP win (1 gold)</p></li>
      <li><p>Interest (0-5 gold)</p></li>
    </ul>
    <p>There are a few different paths you can take when streaking:</p>
    <h3>Lose streaking</h3>
    <p>
      If you have a weak board on 2-1, try to lose streak. When lose streaking,
      you should check other people's boards to ensure you're weaker than them.
      Don't be afraid to sell some of your pairs if it's needed to hit the
      interest threshold. The goal is to hit the interest thresholds as early as
      possible while losing as little hp as possible.
    </p>
    <h3>Win streaking</h3>
    <p>
      If you have a strong board on 2-1, try win streaking in the whole stage.
      With this strategy, you should be less worried about making interest, as
      you need to win as many games as possible. You should be level 4 on 2-1, 5
      at 2-3, or 2-5.
    </p>
    <h3>Mixed steaking</h3>
    <p>
      In this case, you should continue the streak you already have. For
      example, if you win the first round, you will try to win as much as
      possible, and when losing, you will also try to lose streak. Usually, you
      should be level 4 on 2-1, have 10 gold on 2-3, level up to 5 on 3-5 if
      you're win streaking, and have 30 gold on krugs.
    </p>
    <h2>Stage 3</h2>
    <p>
      If you're on a winning streak, level up to 6 on 3-1 to keep winning if you
      will have 30+ gold after the level-up. On 3-2, you should almost always
      level up to 6. The only time you don't level up here is if you're lose
      streaking and planning on rolling at level 7, or you can't add any
      valuable unit on your board.
    </p>
    <p>You can roll on level 6:</p>
    <ul>
      <li><p>if you are win streaking and need a little stronger board</p></li>
      <li><p>you have 3 or more pairs to hit</p></li>
      <li><p>turn around a lose streak into a win streak</p></li>
    </ul>
    <p>
      On 3-5, you can level up to 7 if you will have 30+ gold after. You should
      stay above 30 gold because it's easy to reach 50 from there, but it's much
      more challenging if you have less than 30 gold.
    </p>
    <h2>Stage 4</h2>
    <p>
      In stage 4, people start to have less hp and take more damage. Most of the
      time, you want to be level 7 on 4-1. This is when you decide if you should
      roll or if you can go for a fast 8.
    </p>
    <p>This is when to roll at level 7:</p>
    <ul>
      <li><p>you have 60 hp or less</p></li>
      <li><p>you have a weak board</p></li>
      <li><p>you have 3/4 cost carry in your comp</p></li>
      <li><p>you don't depend on 5 cost units</p></li>
      <li><p>others are rolling</p></li>
    </ul>
    <p>
      If you roll down, you should stop at 10-20 gold. If you go below that, you
      most likely won't be able to go level 8 in that game. Only roll to 0 if
      you're weak or have low hp.
    </p>
    <h3>Fast 8 strategy</h3>
    <p>
      You can level up to 8 on 4-2, 4-5, or 5-1. This depends on your total hp
      and gold. Do this if you stay above 30-40 gold after leveling, so you will
      have enough gold to hit the units you need.
    </p>
    <h2>Treasure Dragon</h2>
    <p>
      You should have at least 10 gold before entering 4-7 to have enough gold
      to roll for the items you need.
    </p>
    <h2>Stage 5</h2>
    <p>
      If you haven't leveled up to 8 yet, you should do it on 5-1. Almost always
      roll down to 0 to hit the units you need. If you high rolled through your
      game and have enough hp and gold, you can go to fast 9 on 5-5 or 6-1.
      Otherwise, you keep rolling every turn to hit the two-star units you need.
    </p>
    <h2>Slow roll comps</h2>
    <h3>1-Cost Reroll Comp Leveling</h3>
    <p>
      Don't put any gold into levels at the start of the game. It is recommended
      to go on a loss-steak when you play this type of comp. On 3-2, when you're
      level 5, start slow rolling to hit the unit you need. But, again, make
      sure to stay above 50 gold. It doesn't really matter when you level up to
      6 or 7. When you hit the units you need, try to turn your lose streak into
      a win streak.
    </p>
    <h3>2-Cost Reroll Comp Leveling</h3>
    <p>
      Here you can level up on 2-1 if you want. Try to go for a streak if you
      can. Doesn't matter too much if it's a lose streak or a win streak. What
      matters is that you should slow roll on level 6 while staying above 50
      gold. Try to level up to 6 on stages 3-2 or 3-5. Then, when you hit your
      desired units, start putting gold into levels.
    </p>
    <h3>3-Cost Reroll Comp Leveling</h3>
    <p>
      Same as the 2-cost reroll comp, the game starts after level 6. Try to
      minimize your hp loss while maintaining a healthy economy. Level up to 6
      on 3-2 and stabilize your board if needed. On 4-1, level up to 7 and start
      slow rolling. Level 7 is the best time to roll because that's when you
      have the most chance to his 3-star units in the shop.
    </p>
    <h2>Champion pool</h2>
    <p>
      There are a fixed amount of champions in each rarity. Therefore, the
      higher the cost of the unit, the least is in the pool.
    </p>

    <ul>
      <li><p>1 cost champs - 29 units</p></li>
      <li><p>2 cost champs - 22 units</p></li>
      <li><p>3 cost champs - 18 units</p></li>
      <li><p>4 cost champs - 12 units</p></li>
      <li><p>5 cost champs - 10 units</p></li>
    </ul>
    <p>
      So next time you're wondering why can't you hit a specific unit, try to
      look around, and maybe you will find that the unit you're looking for is
      out of the pool.
    </p>
