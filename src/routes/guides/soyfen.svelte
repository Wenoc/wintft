  <h1>Soy'fen + Jade Guide</h1>
  <div class="blogBody">
    <p><strong>Note:</strong> This guide was written in patch 12.20</p>
    <p>
      This comp's strength is that it's solid. It doesn't rely on 3* units, and
      you can often secure 1st place if you play it correctly.
    </p>
    <p>Its weakness is that it's challenging to get the right items.</p>
    <p>
      Depending on what you get first, you put your carry items on SOY or Sy'fen
    </p>
    <h2>Best items:</h2>
    <p>
      SOY - Bloodthirster, Titans Resolve, Edge of Night <br />
      Sy'fen - Bloodthirster, Titans Resolve, Giant Slayer
    </p>
    <p>
      Put the rest of your items on Jayce. You can use him as a tank or an AP
      carry.
    </p>
    <p>
      Tank: Gargoyle's Stoneplate, Sunfire Cape, Warmog's Armor <br />
      Ap: Archangel's Staff, Rabadon's Deathcap, Morellonomicon
    </p>
    <h2>Early game:</h2>
    <p>
      A typical opener to play the Soy'fen comp is Nidalee or Wukong as item
      holders.
    </p>
    <p>
      Your early game board should look like this: Nidalee, Skarner, Vladimir,
      Gnar.
    </p>
    <h2>Mid game:</h2>
    <p>
      Keep using astral and activate Jade when you get the chance <br />
      (Nidalee, Skarner, Vladimir, Gnar, Wukong, Jax)
    </p>
    <p>
      You want to push levels to 7 & 8 as soon as possible to hit the required
      units.
    </p>
    <p>At the latest level up to 7 at 4-1, roll down to find SOY & Sy'fen.</p>
    <h2>Late game:</h2>
    <p>
      Your final board will look like this: Gnar, Jax, SOY, Sy'fen, Jayce,
      Pantheon
    </p>
    <p>In the late game, you want to 2* all of your units.</p>
    <p>Slam the rest of the items you get on Jayce and Pantheon.</p>
    <p>If you manage to go LVL 9, you can put in any tier 5 unit.</p>
    <h2>Positioning:</h2>
    <p>Make sure Sy'fen and SOY get the Jade statue buff.</p>
    <p>
      Put Sy'fen on the front line, but make sure he doesn't get targeted first.
    </p>

    <h2>Augments:</h2>
    <ul>
      <li>Portable forge - Death's Defiance on SOY or Sy'fen</li>
      <li>Knife's Edge</li>
      <li>Celestial Blessing</li>
      <li>Ascension</li>
    </ul>
