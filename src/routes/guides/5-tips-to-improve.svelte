  <h1>5 Tips to Improve in TFT</h1>
  <div class="blogBody">
    <p>
      If you have trouble reaching higher ranks in TFT, maybe you will find some
      tips that you don't apply in your games. I hope you will learn something
      new!
    </p>
    <h2>1. Scouting</h2>
    <p>
      From the start, you have to look at other players' boards. For example, if
      you want to loss-streak, you must confirm that others have stronger boards
      than you.
    </p>
    <p>
      You also have to look at other people's positioning. You don't want
      assassins to jump on your carry at the start of the fight.
    </p>
    <h2>2. Positioning</h2>
    <p>
      As I mentioned, you want to avoid assassins jumping on your carry unit,
      but there are other tips.
    </p>
    <ul>
      <li>Spread out your units if the enemy's carry deals splash damage.</li>
      <li>
        Don't put your carries on the backline if you see a Zyra in someone's
        comp.
      </li>
      <li>Avoid zephyr and shroud.</li>
    </ul>
    <h2>3. Tempo</h2>
    <p>
      Every lobby has a pace, depending on what augments most players choose or
      how lucky they were in stage 1. Of course, not all players care about
      these factors, and they play the same way every game.
    </p>
    <p>
      Slow tempo means most players focus on building econ and spike later in
      the game.
    </p>
    <p>
      Fast tempo means most players focus on making their board as strong as
      possible before every fight.
    </p>
    <p>
      If you plan to build econ, but the rest of the lobby is leveling up and
      rolling in stage 2. You will bleed out very soon if you don't follow their
      pace.
    </p>
    <p>
      I could write a whole post about tempo, but I want to keep it short. Maybe
      in the future.
    </p>
    <h2>4. Strongest Board</h2>
    <p>
      Strongest Board is an essential concept that many players don't apply. It
      means that you have the play the strongest board every fight. You
      shouldn't be fixed on one specific opener or comp.
    </p>
    <p>
      Maybe you have Twitch as an item holder. You're looking for Xayah, but the
      game only gives you Nilah. Don't be afraid to use Nilah instead of Xayah
      until you get her. Nilah is a solid unit without traits and can hold
      Xayah's items.
    </p>
    <h2>5. Slamming Items</h2>
    <p>
      Every team comp you see on the team comps page has items below the carry
      units. Those are the OPTIMAL items.
    </p>
    <p>
      A general rule in TFT is not to have four or more item components on your
      bench. At that point, you should slam the best items you can make.
    </p>
    <p>
      You shouldn't chase the perfect items if the game doesn't give them to
      you.
    </p>
    <p>
      A perfect Xayah build would be (Giant Slayer, Guinsoo, Runnans) But (Last
      Whisper, Infinity Edge, Guinsoo) is perfectly fine.
    </p>
